#include "ft_list.h"

t_list		*ft_list_push_params(int ac, char **av)
{
	t_list *l;

	l = 0;
	while (ac > 1)
	{
		ft_list_push_back(&l, av[ac - 1]);
		ac--;
	}
	return (l);
}
